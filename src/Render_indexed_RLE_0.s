.text
    li s0, LED_MATRIX_0_BASE     # Framebuffer base
    li s1, LED_MATRIX_0_SIZE     # Total framebuffer bytes
    add s1, s1, s0               # End address
    la t0, palette               # Palette base
    la t1, image_data            # RLE stream pointer

draw_loop:
    beq s0, s1, draw_done        # Stop when screen full
    lbu t2, 0(t1)                # Read RLE byte (Paint X times | Color Y)
    addi t1, t1, 1               # Next byte
    
    # Extract count
    srli t3, t2, 4               # t3 = "how many times do I paint?" (first 4 bits)
    
    # Extract color index
    andi t4, t2, 0x0F            # t4 = "which color?" (last 4 bits)
    
    # Palette lookup
    slli t4, t4, 2               # Multiply by 4 (each color takes 4 bytes)
    add t4, t0, t4               # Exact address of the color
    lw t5, 0(t4)                 # Load RGB color

pixel_loop:
    sw t5, 0(s0)                 # Write pixel
    addi s0, s0, 4               # Move to next pixel
    addi t3, t3, -1              # Decrement counter (painted one, X left)
    bnez t3, pixel_loop          # Still need to paint? Yes ? keep painting
    j draw_loop

draw_done:
    nop
